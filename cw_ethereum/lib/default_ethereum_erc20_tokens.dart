          try {
            iconPath = CryptoCurrency.all
                .firstWhere((element) => element.title.toUpperCase() == token.symbol.toUpperCase())
                .iconPath;
          } catch (_) {}
        } else {
          iconPath = token.iconPath;
        }

        return Erc20Token.copyWith(token, icon: iconPath, tag: 'ETH');
      }).toList();
